#include <iostream>
#include <math.h>
using namespace std;

int main() {
int x = 7;
int y = 6;
if (x > y) {
    cout << "x is greater than y." << endl;
} else if (x < y) {
    cout << "x is less than y." << endl;
} else if (x == y){
    cout << "x and y are equal." << endl;

} 




}
